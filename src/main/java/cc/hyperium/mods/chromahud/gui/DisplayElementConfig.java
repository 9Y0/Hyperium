    @Override
    protected void actionPerformed(GuiButton button) throws IOException {
        Consumer<GuiButton> guiButtonConsumer = clicks.get(button);
        if (guiButtonConsumer != null) {
            guiButtonConsumer.accept(button);
        }

    }

    @Override
    public void updateScreen() {
        ScaledResolution current = ResolutionUtil.current();
        if (current.getScaledWidth() != lastWidth || current.getScaledHeight() != lastHeight) {
            repack();
            lastWidth = current.getScaledWidth();
            lastHeight = current.getScaledHeight();
        }
        if (element.isRGB())
            element.recalculateColor();
        for (GuiButton guiButton : buttonList) {
            Consumer<GuiButton> guiButtonConsumer = updates.get(guiButton);
            if (guiButtonConsumer != null) {
                guiButtonConsumer.accept(guiButton);
            }
        }
    }

    @Override
    protected void mouseClicked(int mouseX, int mouseY, int mouseButton) throws IOException {
        super.mouseClicked(mouseX, mouseY, mouseButton);
        apply(mouseX, mouseY);
    }

    public float scale() {
        return availableSpace() / 285F;
    }

    private void apply(int mouseX, int mouseY) {
        if(mouseLock)
            return;
        if (!Mouse.isButtonDown(0))
            return;
        if (!element.isColorPallet())
            return;
        ScaledResolution current = ResolutionUtil.current();
        float scale = scale();
        int left = posX(1);
        int right = posX(2);
        int top = posY(1);
        int bottom = posY(3);
        float x = 0;
        float y = 0;
        if (mouseX > left && mouseX < right) {
            if (mouseY > top && mouseY < bottom) {
                x = mouseX - left;
                y = mouseY - top;

                x /= scale;
                y /= scale;
                if (y > 0 && y <= 256)
                    if (x < 256 && x > 0) {
                        this.hue = (int) x;
                        this.saturation = (int) (256 - y);
                        System.out.println("Update: " + hue + " sat: " + saturation);
                        regenImage();
                        lastX = mouseX;
                        lastY = mouseY;
                    } else if (x > 256 + 15 && x < 256 + 15 + 15) {
                        System.out.println(y);
                        this.brightness = (int) y;
                        regenImage();
                    }
            }
        }

        //Coords within the box they clicked adjusted for scale


    }

